class Solution {
public:
bool dfsColor(int x,int col,vector<int>&color,vector<vector<int>>&graph){
        color[x]=col;
        for(auto it:graph[x]){
                if(color[it]==-1){
                        if(!dfsColor(it,col^1,color,graph)){
                                return false;
                        }
                }
                else if(color[it]==col){
                        return false;
                }
        }
        return true;
}
bool isBipartite(vector<vector<int>>& graph) {
        int n=graph.size();
        vector<int>color(n,-1);
        for(int i=0;i<n;i++){
                if(color[i]==-1){
                        if(!dfsColor(i,0,color,graph)){
                                return false;
                        }
                }
        }
        return true;
    }
};